
public class DateTwo {
	private int month;
	private int date;
	private int year;
	
	
	public void setMonth(int m){
		this.month = month;
	}
	
	public void setDate(int date){
		this.date = date;
	}
	
	public void setYear(int y){
		year = y;
	}

	public int getMonth(){
		return month;
	}
	
	public int getDate(){
		return date;
	}
	public int getYear(){
		return year;
	}
	
	
	

}
